/*
# AquaTrak — Complete Database Schema

## Overview
Full schema for AquaTrak, a lean SMB operations platform for water tank cleaning businesses.
Single-tenant design: one merchant operates the system. All data is accessible to the
anon-key client (no auth UI in scope).

## New Tables
1. **merchants** — Business owner profile
2. **staff** — Employee records with daily wage
3. **customers** — Customer records with tank details and location
4. **service_cards** — Service job tracking with status lifecycle
5. **attendance** — Staff check-in/check-out with geofence verification
6. **inventory** — Chemical supply stock levels with minimum thresholds
7. **inventory_transactions** — Audit log for every stock deduction
8. **service_inventory_requirements** — Per-service-type chemical usage rates per 1000L
9. **stock_alerts** — Low-stock notifications generated by trigger
10. **cron_logs** — Execution log for the daily reminder cron job
11. **support_tickets** — AI FAQ and human escalation ticket tracking

## Triggers
- **auto_deduct_inventory**: After service_cards.job_status changes to 'completed',
  automatically deducts inventory based on tank capacity x per-1000L requirement rates,
  and creates stock_alerts if stock falls below threshold.

## Security
- RLS enabled on every table.
- All tables use `TO anon, authenticated` with `USING (true)` - single-tenant app,
  no sign-in required, data is intentionally shared within the business.

## Seed Data
- 1 sample merchant (Sunil Sharma, AquaClean Services)
- 2 staff members
- 3 sample customers with varying tank sizes
- 3 service cards (1 pending, 1 in_progress, 1 completed)
- 2 inventory items (Chlorine 500g, Bleaching Powder 2000g)
- Service inventory requirements for standard_cleaning service type
*/

