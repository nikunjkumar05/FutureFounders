-- Policies for all tables (single-tenant: fully accessible)
DO $$ BEGIN
  DECLARE tbl text;
  BEGIN
    FOREACH tbl IN ARRAY ARRAY[
      'merchants','customers','service_cards','staff','attendance',
      'inventory','inventory_transactions','service_inventory_requirements',
      'stock_alerts','cron_logs','support_tickets'
    ] LOOP
      EXECUTE format('DROP POLICY IF EXISTS "anon_select_%s" ON %I', tbl, tbl);
      EXECUTE format('DROP POLICY IF EXISTS "anon_insert_%s" ON %I', tbl, tbl);
      EXECUTE format('DROP POLICY IF EXISTS "anon_update_%s" ON %I', tbl, tbl);
      EXECUTE format('DROP POLICY IF EXISTS "anon_delete_%s" ON %I', tbl, tbl);
      EXECUTE format('CREATE POLICY "anon_select_%s" ON %I FOR SELECT TO anon, authenticated USING (true)', tbl, tbl);
      EXECUTE format('CREATE POLICY "anon_insert_%s" ON %I FOR INSERT TO anon, authenticated WITH CHECK (true)', tbl, tbl);
      EXECUTE format('CREATE POLICY "anon_update_%s" ON %I FOR UPDATE TO anon, authenticated USING (true) WITH CHECK (true)', tbl, tbl);
      EXECUTE format('CREATE POLICY "anon_delete_%s" ON %I FOR DELETE TO anon, authenticated USING (true)', tbl, tbl);
    END LOOP;
  END;
END $$;

-- ============================================================
-- TRIGGER: Auto inventory deduction on job completion
-- ============================================================
CREATE OR REPLACE FUNCTION auto_deduct_inventory()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  v_qty integer;
  v_qty_per_1000L numeric;
  v_item_name text;
  v_inv_id uuid;
  v_merchant_id uuid;
  v_new_stock numeric;
  v_min_threshold numeric;
  v_deduct_qty numeric;
BEGIN
  IF NEW.job_status = 'completed' AND (OLD.job_status IS NULL OR OLD.job_status != 'completed') THEN
    IF NEW.quantity IS NOT NULL THEN
      v_qty := NEW.quantity;
    ELSE
      SELECT c.tank_capacity_liters INTO v_qty
        FROM customers c WHERE c.id = NEW.customer_id;
    END IF;

    SELECT c.merchant_id INTO v_merchant_id
      FROM customers c WHERE c.id = NEW.customer_id;

    FOR v_item_name, v_qty_per_1000L IN
      SELECT sir.item_name, sir.quantity_per_1000L
        FROM service_inventory_requirements sir
        WHERE sir.service_type = NEW.service_type
    LOOP
      SELECT i.id, i.current_stock, i.minimum_threshold
        INTO v_inv_id, v_new_stock, v_min_threshold
        FROM inventory i
        WHERE i.item_name = v_item_name AND i.merchant_id = v_merchant_id
        LIMIT 1;

      IF v_inv_id IS NOT NULL THEN
        v_deduct_qty := (v_qty::numeric / 1000.0) * v_qty_per_1000L;
        v_new_stock := v_new_stock - v_deduct_qty;

        UPDATE inventory
          SET current_stock = GREATEST(v_new_stock, 0)
          WHERE id = v_inv_id;

        INSERT INTO inventory_transactions (inventory_id, service_card_id, quantity_deducted)
          VALUES (v_inv_id, NEW.id, v_deduct_qty);

        IF v_new_stock < v_min_threshold THEN
          INSERT INTO stock_alerts (inventory_id, merchant_id, alert_type)
            VALUES (v_inv_id, v_merchant_id, 'low_stock');
        END IF;
      END IF;
    END LOOP;
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trigger_auto_deduct_inventory ON service_cards;
CREATE TRIGGER trigger_auto_deduct_inventory
  AFTER UPDATE ON service_cards
  FOR EACH ROW
  EXECUTE FUNCTION auto_deduct_inventory();

